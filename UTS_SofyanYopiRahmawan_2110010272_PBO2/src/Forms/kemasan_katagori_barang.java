    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                  try {
    if (txtcategoryid.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Id belum di isi");
        txtcategoryid.requestFocus();
    } else if (txtcategory_nama.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "ada belum di isi");
        txtcategory_nama.requestFocus();
    } else if (jTextField1.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, " ada belum di isi");
        jTextField1.requestFocus();
    } else if (jTextField2.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, " ada belum di isi");
        jTextField2.requestFocus();
    } else if (jTextField3.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "ada belum di isi");
        jTextField3.requestFocus();
    } else if (jTextField4.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, " ada belum di isi");
        jTextField4.requestFocus();
    } else {
        String sql = "UPDATE tbl_keuanganprbadi SET Jumlah_Pendapatan=?, Jumlah_Pengeluaran=?, Jumlah_Utang=?, Tanggal_Transaksi=?, Tanggal_Pendapatan=? WHERE Id=?";
try (PreparedStatement preparedStatement = myObject.Konek.prepareStatement(sql)) {
    preparedStatement.setString(1, txtcategory_nama.getText());
    preparedStatement.setString(2, jTextField1.getText());
    preparedStatement.setString(3, jTextField2.getText());
    preparedStatement.setString(4, jTextField3.getText());
    preparedStatement.setString(5, jTextField4.getText());
    preparedStatement.setString(6, txtcategoryid.getText());  // Assuming Id is the last parameter

    int rowsUpdated = preparedStatement.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate");
                tampilDatapackage_category();
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data dengan KODE tersebut");
            }
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, e.toString());
}
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
   
        try {
            if (
                    (txtcategoryid.getText().isEmpty()) &&
                    (txtcategory_nama.getText().isEmpty()) &&
                    (jTextField1.getText().isEmpty()) &&
                    (jTextField2.getText().isEmpty()) &&
                    (jTextField3.getText().isEmpty()) &&
                    (jTextField4.getText().isEmpty())
                ){
                JOptionPane.showMessageDialog(this, "Silahkan Pilih Data Terlebih Dahulu");
            }else
            {
                String sql = "DELETE FROM tbl_keuanganprbadi WHERE Id=?";
                PreparedStatement hapus = myObject.Konek.prepareStatement(sql);
                hapus.setString(1, txtcategoryid.getText());
                hapus.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                tampilDatapackage_category();
            }
        } catch (Exception e){
        }
    }                                        

