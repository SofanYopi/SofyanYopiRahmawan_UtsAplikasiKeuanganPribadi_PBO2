    public void tampilDatapackage_category(){
        try {
            modelTabelpackage_category.getDataVector().removeAllElements();
            modelTabelpackage_category.fireTableDataChanged();
        
            PreparedStatement query = myObject.Konek.prepareStatement("SELECT * FROM tbl_keuanganprbadi");
            ResultSet data = query.executeQuery();
            while (data.next()) {
            Object[] baris = new Object[6];
            baris[0] = data.getString("Id");
            baris[1] = data.getString("Jumlah_Pendapatan");
            baris[2] = data.getString("Jumlah_Pengeluaran");
            baris[3] = data.getString("Jumlah_Utang");
            baris[4] = data.getString("Tanggal_Transaksi");
            baris[5] = data.getString("Tanggal_Pendapatan");
            modelTabelpackage_category.addRow(baris);
            }
            query.close();
            data.close();
            } catch (Exception e){
            }
    }

